class Measurement {
  final double back;
  final double sleeve;
  final double lenght;
  final double neck;
  final double open;
  final double W;
  final double R;
  final double T;
  final double L;
  final double K;
  final double D;

  Measurement({
    this.W = 0,
    this.R = 0,
    this.T = 0,
    this.L = 0,
    this.K = 0,
    this.D = 0,
    required this.back,
    required this.sleeve,
    required this.lenght,
    required this.neck,
    required this.open,
  });
}
